     * 与えられた絵文字からその絵文字の絵文字コードを取得する
     */
    function get_emoji_code4emoji($emoji, $carrier = '')
    {
        $emoji_code = '';
        switch ($carrier) {
            case 'i':
            $converter = OpenPNE_EmojiDocomo::getInstance();
            $emoji_code = $converter->get_emoji_code4emoji($emoji);
            break;
            case 's':
            $converter = OpenPNE_EmojiSoftBank::getInstance();
            $emoji_code = $converter->get_emoji_code4emoji($emoji);
            break;
            case 'e':
            $converter = OpenPNE_EmojiAU::getInstance();
            $emoji_code = $converter->get_emoji_code4emoji($emoji);
            break;
            default:
            //キャリアが指定されていない場合は全てのキャリアでチェックを行う
            $converter = OpenPNE_EmojiDocomo::getInstance();
            $emoji_code = $converter->get_emoji_code4emoji($emoji);
            if (!$emoji_code) {
                $converter = OpenPNE_EmojiSoftBank::getInstance();
                $emoji_code = $converter->get_emoji_code4emoji($emoji);
            }
            if (!$emoji_code) {
                $converter = OpenPNE_EmojiAU::getInstance();
                $emoji_code = $converter->get_emoji_code4emoji($emoji);
            }
            break;
        }
        
        return $emoji_code;
    }
    
    /**
     * 与えられた絵文字のIDを指定されたキャリアの対応する絵文字IDで返す
     * 対応するIDが指定されたキャリア間に存在しない場合はfalseを返す
     */
    function convert_emoji_code_id($o_carrier, $o_id, $c_carrier)
    {
        if ($o_carrier == $c_carrier) {
            //キャリアの変更がない場合はそのままIDを返す
            return $o_id;
        }
        
        if (!$this->relation_list[$o_carrier][$c_carrier][$o_id]['existance']) {
            return false;
        }
        
        return $this->relation_list[$o_carrier][$c_carrier][$o_id]['emoji_code_id'];
    }
    
    /**
     * 与えられた絵文字コードを指定されたキャリアの対応する絵文字IDで返す
     * 対応するIDが指定されたキャリア間に存在しない場合はfalseを返す
     */
    function convert_emoji_code($o_code, $c_carrier)
    {
        $o_carrier = $o_code[0];
        $o_id = substr($o_code, 1);
        
        if ($o_carrier == $c_carrier) {
            //キャリアの変更がない場合はそのままIDを返す
        	return $o_id;
        } else {
        	return $this->convert_emoji_code_id($o_carrier, $o_id, $c_carrier);
        }
    }
    
    /**
     * 与えられた絵文字コードを指定されたキャリアの対応する絵文字で返す
     * 対応する絵文字が指定されたキャリア間に存在しない場合は定められた代替文字を返す
     */
    function convert_emoji($o_code, $c_carrier)
    {
        if ($c_code = $this->convert_emoji_code($o_code, $c_carrier)) {
            switch ($c_carrier) {
                case 'i':
                $converter = OpenPNE_EmojiDocomo::getInstance();
                break;
                case 's':
                $converter = OpenPNE_EmojiSoftBank::getInstance();
                break;
                case 'e':
                $converter = OpenPNE_EmojiAU::getInstance();
                break;
                default:
                return false;
            }
        	return $converter->get_emoji4emoji_code_id($c_code);
        }
        
        //対応する絵文字コードが変換先キャリアにない場合は代替文字を返す
        $o_carrier = $o_code[0];
        $o_id = substr($o_code, 1);
        return $this->relation_list[$o_carrier][$c_carrier][$o_id]['value'];
    }
